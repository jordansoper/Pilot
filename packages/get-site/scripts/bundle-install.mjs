#!/usr/bin/env node
// ---------------------------------------------------------------------------
// bundle-install.mjs — embed install.sh into the worker as a TS module.
//
// Reads /install.sh from the repo root, computes its SHA-256, and writes
// `src/install-bundled.ts` so the worker serves install.sh from the bundle
// (zero cold-start latency, no GitHub dependency on the happy path).
//
// Escaping: install.sh is bash, with many `${VAR}` expansions. We must
// escape those to `\${` so JS doesn't try to interpolate them when the
// worker constructs the response body.
// ---------------------------------------------------------------------------

import { readFileSync, writeFileSync, mkdirSync, statSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createHash } from 'node:crypto';

const HERE = dirname(fileURLToPath(import.meta.url));
const PKG = resolve(HERE, '..');
const REPO = resolve(PKG, '..', '..');
const INSTALL = join(REPO, 'install.sh');
const OUT = join(PKG, 'src', 'install-bundled.ts');

const content = readFileSync(INSTALL, 'utf8');
const sha256 = createHash('sha256').update(content, 'utf8').digest('hex');
const bytes = Buffer.byteLength(content, 'utf8');
const mtime = statSync(INSTALL).mtime.toISOString();

const escapeTsLiteral = (s) =>
  s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

const banner = `// ---------------------------------------------------------------------------
// THIS FILE IS GENERATED BY scripts/bundle-install.mjs — DO NOT EDIT BY HAND.
// Regenerate by running:  pnpm --filter @pilot/get-site bundle
// (also runs on postinstall, prebuild, pretest, pretypecheck, predev.)
//
// Source:    ${INSTALL}
// SHA-256:   ${sha256}
// Bytes:     ${bytes}
// Built at:  ${mtime}
// ---------------------------------------------------------------------------
`;

const out = `${banner}
// Explicit widish type annotations — otherwise TS narrows numeric/string
// literals and downstream comparisons against the constants above get
// flagged as comparisons to a literal value that can never match (e.g.
// the bytes constant would have type 21850 rather than number).
export const INSTALL_SH_SHA256: string = ${JSON.stringify(sha256)};
export const INSTALL_SH_BYTES: number = ${bytes};
export const INSTALL_SH_SOURCE: string = ${JSON.stringify(INSTALL)};
export const INSTALL_SH_TEXT: string = \`${escapeTsLiteral(content)}\`;
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, out, 'utf8');

console.log(
  `[bundle-install] wrote ${OUT} (${bytes} bytes, sha256 ${sha256.slice(0, 12)}…)`,
);
